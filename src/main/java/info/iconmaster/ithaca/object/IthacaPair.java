package info.iconmaster.ithaca.object;

public class IthacaPair extends IthacaObject {
	public IthacaObject head;
	public IthacaObject tail;
	
	public IthacaPair(IthacaObject head, IthacaObject tail) {
		super();
		this.head = head;
		this.tail = tail;
	}
	
	@Override
	public String toString() {
		return "("+head+" . "+tail+")";
	}
}
